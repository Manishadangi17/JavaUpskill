package JavaUpskill;

public class ImplicitCastingExample {
	public static void main(String args[]) {
		        int x = 5;
		        double y = x; // Implicit casting from int to double
		        long z = x;
		        System.out.println("x = " + x);
		        System.out.println("y = " + y);
		        System.out.println("z = " + z);

	}

}
