package JavaUpskill;

public class RelationalOperator {
	public static void main(String args[]) {
		int x = 42;
		int y = 10;
		boolean result1 = x > y;
		boolean result2 = x < y;
		boolean result3 = x >= y;
		boolean result4 = x <= y;
		boolean result5 = x == y;
		System.out.println(result1+" "+result2+" "+result3+" "+result4+" "+result5+"  ");
		
	}

}
