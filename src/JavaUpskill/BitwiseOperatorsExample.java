package JavaUpskill;

public class BitwiseOperatorsExample {
	    private static void main(String[] args) {
	        int x = 10;
	        int y = 5;
	        int result1 = x & y;
	        int result2 = x | y;
	        int result3 = x ^ y;
	        int result4 = ~x;
	        int result5 = x << 2;
	        int result6 = x >> 2;
	        
	        System.out.println("Bitwise AND operator: " + result1);
	        System.out.println("Bitwise OR operator: " + result2);
	        System.out.println("Bitwise XOR operator: " + result3);
	        System.out.println("Bitwise NOT operator: " + result4);
	        System.out.println("Left shift operator: " + result5);
	        System.out.println("Right shift operator: " + result6);
	    }
	    
	}



