package JavaUpskill;

public class UnaryOpEX {
	public class UnaryOperatorsExample {
	    public static void main(String[] args) {
	        int x = 10;
	        int y = -5;
	        int result1 = +x;
	        int result2 = -y;
	        int result3 = ++x;
	        int result4 = --y;
	        
	        System.out.println("plus operator: +" + x + " = " + result1);
	        System.out.println("minus operator: -" + y + " = " + result2);
	        System.out.println("operator: ++" + x + " = " + result3);
	        System.out.println("operator: --" + y + " = " + result4);
	    }
	}


}
