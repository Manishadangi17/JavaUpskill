package JavaUpskill;

public class IfElseIf {
	public static void main(String args[]) {
		int x = 88;
		if (x > 100) {
		    System.out.println("x is very large");
		} else if (x > 50) {
		    System.out.println("x is large");
		} else if (x > 10) {
		    System.out.println("x is medium");
		} else {
		    System.out.println("x is small");
		}	
		
	}

}
